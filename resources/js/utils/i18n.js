export const getLabel = (key, locale = null) => {
    // If locale is not provided, try to get from document, default to 'fr'
    const currentLocale = locale || (typeof document !== 'undefined' ? document.documentElement.lang : 'fr');

    // Check if locale exists in dictionary, else fallback to 'fr'
    const langDict = dictionary[currentLocale] || dictionary['fr'];

    // Return translation or key if not found
    return langDict[key] || key;
};
